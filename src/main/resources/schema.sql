

DROP TABLE IF EXISTS TBL_FORMULA_INGREDIENTS;
CREATE TABLE TBL_FORMULA_INGREDIENTS (
    id INT AUTO_INCREMENT  PRIMARY KEY,
    ingredient_id INT,
    min DOUBLE,
    max DOUBLE,
    result DOUBLE
);

